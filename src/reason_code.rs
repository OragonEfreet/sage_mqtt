use crate::ControlPacketType;

pub enum ReasonCode {
    Success,
    NormalDisconnection,
    GrantedQoS0,
    GrantedQoS1,
    GrantedQoS2,
    DisconnectWithWillMessage,
    NoMatchingSubscribers,
    NoSubscriptionExisted,
    ContinueAuthentication,
    ReAuthenticate,
    UnspecifiedError,
    MalformedPacket,
    ProtocolError,
    ImplementationSpecificError,
    UnsupportedProtocolVersion,
    ClientIdentifierNotValid,
    BadUserNameOrPassword,
    NotAuthorized,
    ServerUnavailable,
    ServerBusy,
    Banned,
    ServerShuttingDown,
    BadAuthenticationMethod,
    KeepAliveTimeout,
    SessionTakenOver,
    TopicFilterInvalid,
    TopicNameInvalid,
    PacketIdentifierInUse,
    PacketIdentifierNotFound,
    ReceiveMaximumExceeded,
    TopicAliasInvalid,
    PacketTooLarge,
    MessageRateTooHigh,
    QuotaExceeded,
    AdministrativeAction,
    PayloadFormatInvalid,
    RetainNotSupported,
    QoSNotSupported,
    UseAnotherServer,
    ServerMoved,
    SharedSubscriptionsNotSupported,
    ConnectionRateExceeded,
    MaximumConnectTime,
    SubscriptionIdentifiersNotSupported,
    WildcardSubscriptionsNotSupported,
}

impl ReasonCode {
    pub fn parse(code: u8, packet_type: &ControlPacketType) -> Option<Self> {
        match (code, packet_type) {
            (0x00, ControlPacketType::CONNACK) => Some(ReasonCode::Success),
            (0x00, ControlPacketType::PUBACK) => Some(ReasonCode::Success),
            (0x00, ControlPacketType::PUBREC) => Some(ReasonCode::Success),
            (0x00, ControlPacketType::PUBREL) => Some(ReasonCode::Success),
            (0x00, ControlPacketType::PUBCOMP) => Some(ReasonCode::Success),
            (0x00, ControlPacketType::UNSUBACK) => Some(ReasonCode::Success),
            (0x00, ControlPacketType::AUTH) => Some(ReasonCode::Success),
            (0x00, ControlPacketType::DISCONNECT) => Some(ReasonCode::NormalDisconnection),
            (0x00, ControlPacketType::SUBACK) => Some(ReasonCode::GrantedQoS0),
            (0x01, ControlPacketType::SUBACK) => Some(ReasonCode::GrantedQoS1),
            (0x02, ControlPacketType::SUBACK) => Some(ReasonCode::GrantedQoS2),
            (0x04, ControlPacketType::DISCONNECT) => Some(ReasonCode::DisconnectWithWillMessage),
            (0x10, ControlPacketType::PUBACK) => Some(ReasonCode::NoMatchingSubscribers),
            (0x10, ControlPacketType::PUBREC) => Some(ReasonCode::NoMatchingSubscribers),
            (0x11, ControlPacketType::UNSUBACK) => Some(ReasonCode::NoSubscriptionExisted),
            (0x18, ControlPacketType::AUTH) => Some(ReasonCode::ContinueAuthentication),
            (0x19, ControlPacketType::AUTH) => Some(ReasonCode::ReAuthenticate),
            (0x80, ControlPacketType::CONNACK) => Some(ReasonCode::UnspecifiedError),
            (0x80, ControlPacketType::PUBACK) => Some(ReasonCode::UnspecifiedError),
            (0x80, ControlPacketType::PUBREC) => Some(ReasonCode::UnspecifiedError),
            (0x80, ControlPacketType::SUBACK) => Some(ReasonCode::UnspecifiedError),
            (0x80, ControlPacketType::UNSUBACK) => Some(ReasonCode::UnspecifiedError),
            (0x80, ControlPacketType::DISCONNECT) => Some(ReasonCode::UnspecifiedError),
            (0x81, ControlPacketType::CONNACK) => Some(ReasonCode::MalformedPacket),
            (0x81, ControlPacketType::DISCONNECT) => Some(ReasonCode::MalformedPacket),
            (0x82, ControlPacketType::CONNACK) => Some(ReasonCode::ProtocolError),
            (0x82, ControlPacketType::DISCONNECT) => Some(ReasonCode::ProtocolError),
            (0x83, ControlPacketType::CONNACK) => Some(ReasonCode::ImplementationSpecificError),
            (0x83, ControlPacketType::PUBACK) => Some(ReasonCode::ImplementationSpecificError),
            (0x83, ControlPacketType::PUBREC) => Some(ReasonCode::ImplementationSpecificError),
            (0x83, ControlPacketType::SUBACK) => Some(ReasonCode::ImplementationSpecificError),
            (0x83, ControlPacketType::UNSUBACK) => Some(ReasonCode::ImplementationSpecificError),
            (0x83, ControlPacketType::DISCONNECT) => Some(ReasonCode::ImplementationSpecificError),
            (0x84, ControlPacketType::CONNACK) => Some(ReasonCode::UnsupportedProtocolVersion),
            (0x85, ControlPacketType::CONNACK) => Some(ReasonCode::ClientIdentifierNotValid),
            (0x86, ControlPacketType::CONNACK) => Some(ReasonCode::BadUserNameOrPassword),
            (0x87, ControlPacketType::CONNACK) => Some(ReasonCode::NotAuthorized),
            (0x87, ControlPacketType::PUBACK) => Some(ReasonCode::NotAuthorized),
            (0x87, ControlPacketType::PUBREC) => Some(ReasonCode::NotAuthorized),
            (0x87, ControlPacketType::SUBACK) => Some(ReasonCode::NotAuthorized),
            (0x87, ControlPacketType::UNSUBACK) => Some(ReasonCode::NotAuthorized),
            (0x87, ControlPacketType::DISCONNECT) => Some(ReasonCode::NotAuthorized),
            (0x88, ControlPacketType::CONNACK) => Some(ReasonCode::ServerUnavailable),
            (0x89, ControlPacketType::CONNACK) => Some(ReasonCode::ServerBusy),
            (0x89, ControlPacketType::DISCONNECT) => Some(ReasonCode::ServerBusy),
            (0x8A, ControlPacketType::CONNACK) => Some(ReasonCode::Banned),
            (0x8B, ControlPacketType::DISCONNECT) => Some(ReasonCode::ServerShuttingDown),
            (0x8C, ControlPacketType::CONNACK) => Some(ReasonCode::BadAuthenticationMethod),
            (0x8C, ControlPacketType::DISCONNECT) => Some(ReasonCode::BadAuthenticationMethod),
            (0x8D, ControlPacketType::DISCONNECT) => Some(ReasonCode::KeepAliveTimeout),
            (0x8E, ControlPacketType::DISCONNECT) => Some(ReasonCode::SessionTakenOver),
            (0x8F, ControlPacketType::SUBACK) => Some(ReasonCode::TopicFilterInvalid),
            (0x8F, ControlPacketType::UNSUBACK) => Some(ReasonCode::TopicFilterInvalid),
            (0x8F, ControlPacketType::DISCONNECT) => Some(ReasonCode::TopicFilterInvalid),
            (0x90, ControlPacketType::CONNACK) => Some(ReasonCode::TopicNameInvalid),
            (0x90, ControlPacketType::PUBACK) => Some(ReasonCode::TopicNameInvalid),
            (0x90, ControlPacketType::PUBREC) => Some(ReasonCode::TopicNameInvalid),
            (0x90, ControlPacketType::DISCONNECT) => Some(ReasonCode::TopicNameInvalid),
            (0x91, ControlPacketType::PUBACK) => Some(ReasonCode::PacketIdentifierInUse),
            (0x91, ControlPacketType::PUBREC) => Some(ReasonCode::PacketIdentifierInUse),
            (0x91, ControlPacketType::SUBACK) => Some(ReasonCode::PacketIdentifierInUse),
            (0x91, ControlPacketType::UNSUBACK) => Some(ReasonCode::PacketIdentifierInUse),
            (0x92, ControlPacketType::PUBREL) => Some(ReasonCode::PacketIdentifierNotFound),
            (0x92, ControlPacketType::PUBCOMP) => Some(ReasonCode::PacketIdentifierNotFound),
            (0x93, ControlPacketType::DISCONNECT) => Some(ReasonCode::ReceiveMaximumExceeded),
            (0x94, ControlPacketType::DISCONNECT) => Some(ReasonCode::TopicAliasInvalid),
            (0x95, ControlPacketType::CONNACK) => Some(ReasonCode::PacketTooLarge),
            (0x95, ControlPacketType::DISCONNECT) => Some(ReasonCode::PacketTooLarge),
            (0x96, ControlPacketType::DISCONNECT) => Some(ReasonCode::MessageRateTooHigh),
            (0x97, ControlPacketType::CONNACK) => Some(ReasonCode::QuotaExceeded),
            (0x97, ControlPacketType::PUBACK) => Some(ReasonCode::QuotaExceeded),
            (0x97, ControlPacketType::PUBREC) => Some(ReasonCode::QuotaExceeded),
            (0x97, ControlPacketType::SUBACK) => Some(ReasonCode::QuotaExceeded),
            (0x97, ControlPacketType::DISCONNECT) => Some(ReasonCode::QuotaExceeded),
            (0x98, ControlPacketType::DISCONNECT) => Some(ReasonCode::AdministrativeAction),
            (0x99, ControlPacketType::CONNACK) => Some(ReasonCode::PayloadFormatInvalid),
            (0x99, ControlPacketType::PUBACK) => Some(ReasonCode::PayloadFormatInvalid),
            (0x99, ControlPacketType::PUBREC) => Some(ReasonCode::PayloadFormatInvalid),
            (0x99, ControlPacketType::DISCONNECT) => Some(ReasonCode::PayloadFormatInvalid),
            (0x9A, ControlPacketType::CONNACK) => Some(ReasonCode::RetainNotSupported),
            (0x9A, ControlPacketType::DISCONNECT) => Some(ReasonCode::RetainNotSupported),
            (0x9B, ControlPacketType::CONNACK) => Some(ReasonCode::QoSNotSupported),
            (0x9B, ControlPacketType::DISCONNECT) => Some(ReasonCode::QoSNotSupported),
            (0x9C, ControlPacketType::CONNACK) => Some(ReasonCode::UseAnotherServer),
            (0x9C, ControlPacketType::DISCONNECT) => Some(ReasonCode::UseAnotherServer),
            (0x9D, ControlPacketType::CONNACK) => Some(ReasonCode::ServerMoved),
            (0x9D, ControlPacketType::DISCONNECT) => Some(ReasonCode::ServerMoved),
            (0x9E, ControlPacketType::SUBACK) => Some(ReasonCode::SharedSubscriptionsNotSupported),
            (0x9E, ControlPacketType::DISCONNECT) => {
                Some(ReasonCode::SharedSubscriptionsNotSupported)
            }
            (0x9F, ControlPacketType::CONNACK) => Some(ReasonCode::ConnectionRateExceeded),
            (0x9F, ControlPacketType::DISCONNECT) => Some(ReasonCode::ConnectionRateExceeded),
            (0xA0, ControlPacketType::DISCONNECT) => Some(ReasonCode::MaximumConnectTime),
            (0xA1, ControlPacketType::SUBACK) => {
                Some(ReasonCode::SubscriptionIdentifiersNotSupported)
            }
            (0xA1, ControlPacketType::DISCONNECT) => {
                Some(ReasonCode::SubscriptionIdentifiersNotSupported)
            }
            (0xA2, ControlPacketType::SUBACK) => {
                Some(ReasonCode::WildcardSubscriptionsNotSupported)
            }
            (0xA2, ControlPacketType::DISCONNECT) => {
                Some(ReasonCode::WildcardSubscriptionsNotSupported)
            }
            _ => None,
        }
    }
}

impl From<ReasonCode> for u8 {
    fn from(value: ReasonCode) -> Self {
        match value {
            ReasonCode::Success | ReasonCode::NormalDisconnection | ReasonCode::GrantedQoS0 => 0x00,
            ReasonCode::GrantedQoS1 => 0x01,
            ReasonCode::GrantedQoS2 => 0x02,
            ReasonCode::DisconnectWithWillMessage => 0x04,
            ReasonCode::NoMatchingSubscribers => 0x10,
            ReasonCode::NoSubscriptionExisted => 0x11,
            ReasonCode::ContinueAuthentication => 0x18,
            ReasonCode::ReAuthenticate => 0x19,
            ReasonCode::UnspecifiedError => 0x80,
            ReasonCode::MalformedPacket => 0x81,
            ReasonCode::ProtocolError => 0x82,
            ReasonCode::ImplementationSpecificError => 0x83,
            ReasonCode::UnsupportedProtocolVersion => 0x84,
            ReasonCode::ClientIdentifierNotValid => 0x85,
            ReasonCode::BadUserNameOrPassword => 0x86,
            ReasonCode::NotAuthorized => 0x87,
            ReasonCode::ServerUnavailable => 0x88,
            ReasonCode::ServerBusy => 0x89,
            ReasonCode::Banned => 0x8A,
            ReasonCode::ServerShuttingDown => 0x8B,
            ReasonCode::BadAuthenticationMethod => 0x8C,
            ReasonCode::KeepAliveTimeout => 0x8D,
            ReasonCode::SessionTakenOver => 0x8E,
            ReasonCode::TopicFilterInvalid => 0x8F,
            ReasonCode::TopicNameInvalid => 0x90,
            ReasonCode::PacketIdentifierInUse => 0x91,
            ReasonCode::PacketIdentifierNotFound => 0x92,
            ReasonCode::ReceiveMaximumExceeded => 0x93,
            ReasonCode::TopicAliasInvalid => 0x94,
            ReasonCode::PacketTooLarge => 0x95,
            ReasonCode::MessageRateTooHigh => 0x96,
            ReasonCode::QuotaExceeded => 0x97,
            ReasonCode::AdministrativeAction => 0x98,
            ReasonCode::PayloadFormatInvalid => 0x99,
            ReasonCode::RetainNotSupported => 0x9A,
            ReasonCode::QoSNotSupported => 0x9B,
            ReasonCode::UseAnotherServer => 0x9C,
            ReasonCode::ServerMoved => 0x9D,
            ReasonCode::SharedSubscriptionsNotSupported => 0x9E,
            ReasonCode::ConnectionRateExceeded => 0x9F,
            ReasonCode::MaximumConnectTime => 0xA0,
            ReasonCode::SubscriptionIdentifiersNotSupported => 0xA1,
            ReasonCode::WildcardSubscriptionsNotSupported => 0xA2,
        }
    }
}
